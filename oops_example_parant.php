<?php

class parent_class{
    protected  function sum($a,$b){
        return $a+$b;
    }
     protected  function minus($a,$b){
        return $a-$b;
    }
     protected  function multiply($a,$b){
        return $a*$b;
    }
    protected  function divide($a,$b){
        return $a/$b;
    }
    
}